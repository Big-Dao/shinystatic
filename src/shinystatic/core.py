# Express app generation
# ═══════════════════════════════════════════════

EXPRESS_HEADER = '''"""Shiny Express app (generated by shinystatic)"""
import json
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import matplotlib as mpl
from shiny import reactive
from shiny.express import input, render, ui
'''


def generate_express(source: str) -> str:
    """Generate a standalone Shiny Express app from .qmd source."""
    _, body_source = parse_yaml(source)
    cells = parse_cells(body_source)
    imports = detect_imports(cells)

    parts = [EXPRESS_HEADER, imports, "\n\n"]
    parts.append('mpl.rcParams["font.sans-serif"] = ["Noto Sans CJK SC", "Microsoft YaHei", "SimHei"]\n')
    parts.append('mpl.rcParams["axes.unicode_minus"] = False\n')
    parts.append('mpl.rcParams["figure.dpi"] = 110\n')

    for cell in cells:
        if cell.get("type") == "python" and cell.get("label", "").startswith("params-"):
            parts.append(f"\n{cell['code']}\n")
            break

    seen_labels = set()
    for cell in cells:
        if cell.get("type") != "python":
            continue
        label = cell.get("label")
        if label in seen_labels:
            continue
        seen_labels.add(label)
        if label is None:
            if "shiny" not in cell["code"] and "mpl.rc" not in cell["code"]:
                parts.append(f"\n{cell['code']}\n")
        elif label.startswith(("model-", "text-", "fig-", "tbl-", "ui-", "export-")):
            parts.append(f"\n{cell['code']}\n")

    return "".join(parts)
